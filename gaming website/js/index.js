var total=0;
var i = 0;
function addItem1(){
    var table=document.getElementById('tableItems');
    var row = table.insertRow(1);
    var cell1 = row.insertCell(0);
    var cell2 = row.insertCell(1);
    //var cell3 = row.insertCell(2);
    var name = document.getElementById('itemName').innerHTML;
    var price = document.getElementById('price').innerHTML;
    cell1.innerHTML = name;
    cell2.innerHTML = price + '$';
//    cell3.innerHTML = "<button id='myBtn' onclick='removethisrow(this)'>Remove</button>";
    price=parseInt(price);
    total = total + price;
    var output=document.getElementById('totalTable');
    output.innerHTML=parseInt(total)+'$';
    i++;
}
